package kr.lul.commons.util;

/**
 * @author justburrow
 * @since 2018. 9. 20.
 */
public interface PrimitiveBoxer<S, T> extends Converter<S, T> {
    T box(S primitive);

    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // kr.lul.commons.util.Converter
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    @Override
    default T convert(S source) throws ConvertException {
        return box(source);
    }
}
